public class Card {

	private String cardn;
	private int cardNo;
	public Card(String cardn, int cardNo) {
		this.cardn = cardn;
		this.cardNo = cardNo;
	}
	public String getCardn() {
		return cardn;
	}
	public void setCardn(String cardn) {
		this.cardn = cardn;
	}
	public int getCardNo() {
		return cardNo;
	}
	public void setCardNo(int cardNo) {
		this.cardNo = cardNo;
	}
	
}
